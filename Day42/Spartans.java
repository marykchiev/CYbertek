package Day42;

public class Spartans {

	public static void main(String[] args) {
		int sum1=0;
		String sum2="";
		// If we add 300 random people
		for(int i=1;i<=300;i++)
		{
			int a=1;
			sum1=sum1+a;
		}
		System.out.println("If we add 300 random people: " +sum1);
		// If we add 300 Spartans
		for(int i=1;i<=300;i++)
		{
			int b=1;
			sum2=sum2+b;
		}
		System.out.println("If we add 300 Spartans: " +sum2);
				

	}

}
